package model;

public class CouterModel {
	private int first_value;
	private int second_value;
	private int answer_value;
	
	public CouterModel() {
		this.first_value =0;
		this.second_value =0;
		this.answer_value =0;
	}

	public int getFirst_value() {
		return first_value;
	}

	public void setFirst_value(int first_value) {
		this.first_value = first_value;
	}

	public int getSecond_value() {
		return second_value;
	}

	public void setSecond_value(int second_value) {
		this.second_value = second_value;
	}

	public int getAnswer_value() {
		return answer_value;
	}

	public void setAnswer_value(int answer_value) {
		this.answer_value = answer_value;
	}
	
	
}

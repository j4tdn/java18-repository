package datetime;

public class DeprecateDemo {
	public static void main(String[] args) {
		System.out.println("sum2: " + sum(5, 8));
		System.out.println("sum2: " + sum(2, 3));
		System.out.println("sum2: " + sum(2, 3));
		System.out.println("sum2: " + sum(2, 3));
		
	}

	public static int sum(int a, int b) {
		return a + b;
	}

}
